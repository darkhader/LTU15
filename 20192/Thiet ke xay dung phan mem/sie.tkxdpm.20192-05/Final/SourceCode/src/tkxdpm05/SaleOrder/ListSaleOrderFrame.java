    private void initUI() {
        this.setTitle(this.getClass().getSimpleName());
        this.setDefaultCloseOperation(HIDE_ON_CLOSE);
        saleOrderFrame = new SaleOrderDetailFrame();
        listSaleOrderDTOs = new ArrayList<>();
        showListSaleOrder();
    }

    private void showListSaleOrder() {
        tableListSaleOrderModel = new DefaultTableModel(tableHeader, 0);
        
        tableListSaleOrder.setModel(tableListSaleOrderModel);

        listSaleOrderDTOs = SaleOrderController.getInstance().readAllSaleOrder();
        
        for (SaleOrderDTO saleOrderDTO : listSaleOrderDTOs) {
            String[] data = new String[3];
            data[0] = saleOrderDTO.getSaleOrderId() + "";
            data[1] = saleOrderDTO.getDeliveryDesired();
            data[2] = saleOrderDTO.getStatus();
            tableListSaleOrderModel.addRow(data);
        }
    }
