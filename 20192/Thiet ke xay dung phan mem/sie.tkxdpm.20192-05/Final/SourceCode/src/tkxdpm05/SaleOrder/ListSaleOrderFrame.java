    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int index = tableListSaleOrder.getSelectedRow();
        if (index < 0) {
            return;
        }
        SaleOrderDetailFrame saleOrderDetailFrame = new SaleOrderDetailFrame();
        saleOrderDetailFrame.setVisible(true);
        saleOrderDetailFrame.setsaleOrderDTO(listSaleOrderDTOs.get(index));
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int index = tableListSaleOrder.getSelectedRow();
        if (index < 0) {
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this,
                "Delete " + listSaleOrderDTOs.get(index).getSaleOrderId(),
                "Confirm Delete",
                JOptionPane.OK_CANCEL_OPTION);
        if (confirm == JOptionPane.OK_OPTION) {
            SaleOrderController.getInstance().deleteSaleOrder(listSaleOrderDTOs.get(index));
            showListSaleOrder();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

