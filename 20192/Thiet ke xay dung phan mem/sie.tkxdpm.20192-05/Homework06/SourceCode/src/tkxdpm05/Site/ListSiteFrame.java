    private void initUI() {
        this.setTitle(this.getClass().getSimpleName());
        this.setDefaultCloseOperation(HIDE_ON_CLOSE);
        siteFrame = new SiteFrame();
        listSiteDTOs = new ArrayList<>();
        showListSite();
    }

    private void showListSite() {

        String[] columnNames = new String[]{"ID", "Name", "Ship Day", "Air Day"};
        tableSiteModel = new DefaultTableModel(columnNames, 0);
        tableSite.setModel(tableSiteModel);

        listSiteDTOs = SiteController.getInstance().readAllSite();
        for (SiteDTO siteDTO : listSiteDTOs) {
            String[] data = new String[4];
            data[0] = siteDTO.getSiteId() + "";
            data[1] = siteDTO.getDispName();
            data[2] = siteDTO.getShipDay() + "";
            data[3] = siteDTO.getAirDay() + "";
            tableSiteModel.addRow(data);
        }

    }
