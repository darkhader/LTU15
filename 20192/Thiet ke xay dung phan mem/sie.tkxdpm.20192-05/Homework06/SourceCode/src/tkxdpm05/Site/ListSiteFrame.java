    private void btnDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailActionPerformed
        siteFrame.setVisible(false);
        int index = tableSite.getSelectedRow();
        if (index < 0) {
            return;
        }
        siteFrame.setSiteDTO(listSiteDTOs.get(index));
        siteFrame.setVisible(true);
    }//GEN-LAST:event_btnDetailActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int index = tableSite.getSelectedRow();
        if (index < 0) {
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this,
                "Delete " + listSiteDTOs.get(index).getDispName(),
                "Confirm Delete",
                JOptionPane.OK_CANCEL_OPTION);
        if (confirm == JOptionPane.OK_OPTION) {
            SiteController.getInstance().deleteSite(listSiteDTOs.get(index));
            showListSite();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

